class Particle{
    constructor(x,y,directionX, directionY, size, colour, canvas, ctx){
        this.x = x;
        this.y = y;
        this.directionX = directionX;
        this.directionY = directionY;
        this.size = size;
        this.colour = colour;
        this.canvas = canvas;
        this.ctx = ctx;
    }
}